public class AppDetailClass {
    public AppDetailClass(){

    }
    public String APP_ID;
    public String APP_NAME;
    public String APP_CATEGORY;
    public String APP_ICON;
    public String PUBLISH_ID;

    public String getAPP_ID() {
        return APP_ID;
    }

    public void setAPP_ID(String APP_ID) {
        this.APP_ID = APP_ID;
    }

    public String getAPP_NAME() {
        return APP_NAME;
    }

    public void setAPP_NAME(String APP_NAME) {
        this.APP_NAME = APP_NAME;
    }

    public String getAPP_CATEGORY() {
        return APP_CATEGORY;
    }

    public void setAPP_CATEGORY(String APP_CATEGORY) {
        this.APP_CATEGORY = APP_CATEGORY;
    }

    public String getAPP_ICON() {
        return APP_ICON;
    }

    public void setAPP_ICON(String APP_ICON) {
        this.APP_ICON = APP_ICON;
    }

    public String getPUBLISH_ID() {
        return PUBLISH_ID;
    }

    public void setPUBLISH_ID(String PUBLISH_ID) {
        this.PUBLISH_ID = PUBLISH_ID;
    }
}
