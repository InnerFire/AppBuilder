public class ProductDetailsAdapter {
    public ProductDetailsAdapter() {

    }

    public String Imagename;
    public String Product_Name;
    public String Product_MRP_Price;
    public String Product_Selling_Price;
    public String Product_Discount;
    public String Product_Specification;

    public String getImagename() {
        return Imagename;
    }

    public void setImagename(String imagename) {
        Imagename = imagename;
    }

    public String getProduct_Name() {
        return Product_Name;
    }

    public void setProduct_Name(String product_Name) {
        Product_Name = product_Name;
    }

    public String getProduct_MRP_Price() {
        return Product_MRP_Price;
    }

    public void setProduct_MRP_Price(String product_MRP_Price) {
        Product_MRP_Price = product_MRP_Price;
    }

    public String getProduct_Selling_Price() {
        return Product_Selling_Price;
    }

    public void setProduct_Selling_Price(String product_Selling_Price) {
        Product_Selling_Price = product_Selling_Price;
    }

    public String getProduct_Discount() {
        return Product_Discount;
    }

    public void setProduct_Discount(String product_Discount) {
        Product_Discount = product_Discount;
    }

    public String getProduct_Specification() {
        return Product_Specification;
    }

    public void setProduct_Specification(String product_Specification) {
        Product_Specification = product_Specification;
    }
}
